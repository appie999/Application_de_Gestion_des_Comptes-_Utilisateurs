public class Role {

    private int id;
    private String rolnom;

    public Role(int id, String rolnom) {
        this.id = id;
        this.rolnom = rolnom;
    }

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getrolnom() {
        return rolnom;
    }
    public void setrolnom(String rolnom) {
        this.rolnom = rolnom;
    }


}
